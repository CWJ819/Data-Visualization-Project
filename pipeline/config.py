# ════════════════════ 体裁规则 ════════════════════
_GUTI_KW = ['歌', '行', '吟', '引', '曲', '谣', '辞', '篇', '乐府', '古风', '古意']


def classify_tang_form(title, paragraphs):
    """据句数/字数推断唐诗体裁（输入应为简体）。"""
    if not paragraphs:
        return "未知"
    all_lines = []
    for p in paragraphs:
        for s in re.split(r'[，。！？；：、]', p):
            s = s.strip()
            if s:
                all_lines.append(s)
    if not all_lines:
        all_lines = paragraphs
    char_counts = [len(l) for l in all_lines]
    n = len(all_lines)
    avg = sum(char_counts) / n if n else 0
    is_guti = any(k in title for k in _GUTI_KW)

    if n == 4:
        if all(4 <= c <= 6 for c in char_counts):
            return "五言绝句"
        if all(6 <= c <= 8 for c in char_counts):
            return "七言绝句"
        return "绝句"
    if n == 8:
        if all(4 <= c <= 6 for c in char_counts):
            return "五言律诗"
        if all(6 <= c <= 8 for c in char_counts):
            return "七言律诗"
        return "律诗"
    if is_guti:
        return "古体诗·歌行"
    # 排律：非歌行、n>8、全部行字数完全一致（避免将混合字数的古体误归排律）
    if n > 8 and len(set(char_counts)) == 1:
        return "五言排律" if avg < 6 else "七言排律"
    if avg <= 5:
        return "古体诗·五言"
    if avg <= 8:
        return "古体诗·七言"
    return "古体诗·杂言"


def classify_song_form(rhythmic):
    return f"词·{rhythmic}" if rhythmic else "词"


